const Suma = (num1: number, num2: number): number => num1 + num2;

const Resta = (num1: number, num2: number): number => num1 - num2;

const Multiplicacion = (num1: number, num2: number): number => num1 * num2;

const Divicion = (num1: number, num2: number): number => num1 / num2;

export { Suma, Resta, Multiplicacion, Divicion };
